package operatorji;

public class BitniOperatorji {

  public static void main(String[] args) {
    int a = 10;
    int b = 7;

    System.out.printf("%d | %d = %d \n", a, b, a | b);
    System.out.printf("%d & %d = %d \n", a, b, a & b);
    System.out.printf("%d ^ %d = %d \n", a, b, a ^ b);
    System.out.printf("~ %d = %d \n", a, ~a);
  }
}
